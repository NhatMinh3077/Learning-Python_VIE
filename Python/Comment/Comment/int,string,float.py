print("Công dụng của int()")
# Dùng int() để biến chuỗi thành số để cộng cho nhau
# Lưu ý int() dành cho số nguyên

python14 = int("69") + 5
print(python14)

print("Công dụng của str()")
# Dùng để biến số thành chuỗi để cộng cho nhau
# Giá trị thay đổi mỗi khi chạy lại
python15 = str("69") + "5"
print(python15)

print("Công dụng của float()")
# Tương tự như int() và str() ta dùng float()
python16 = float(69) + 6.9
print(python16)
print("Thay số 0 cho chữ o ở World")
# Float là số thực
# Intergers là nguyên
# String là chuỗi

