# X + Y - Tổng của X với Y
# Ví dụ
a = 1 + 2
print(a)
# X - Y - Hiệu của X với Y
#Ví dụ
b = 2 - 1
print(b)
# X * Y - Tích của X với Y
#Ví dụ
c = 2 * 2
print(c)
# X / Y - Thương của X với Y (kết quả luôn luôn là một số thức)
# Ví dụ
d = 4 / 2
print(d)
# X // Y - Thương nguyên của X với Y (kết quả luôn luôn lớn hơn hoặc bằng X)
# Ví dụ
e = 10 // 3
print(e)
#X % Y - Dư của thương với X
#Ví dụ
f = 10 % 3
print(f)
#X ** Y - Lũy thừa X với cơ số Y
# Ví dụ 
m = 10 ** 3
print(m)